package tr1;
public class TCPBase {
	
	private int porta;
	private String endereco;

	
	public void setPorta(int porta){
		this.porta = porta;
	}
	
	public void setEndereco(String endereco){
		this.endereco=endereco;
	}
	
	public int getPorta(){
		return porta;
	}
	
	public String getEndereco(){
		return endereco;
	}
};
	


